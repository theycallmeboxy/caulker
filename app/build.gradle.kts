// Hilt's hiltJavaCompile task passes all KSP processor JARs to javac's annotation processor
// path, which causes Moshi's legacy APT processor to print a deprecation warning even though
// we use KSP for codegen. Remove moshi-kotlin-codegen from that specific task's AP path.
afterEvaluate {
    tasks.withType<JavaCompile>().configureEach {
        if (name.startsWith("hiltJavaCompile")) {
            options.annotationProcessorPath = options.annotationProcessorPath?.filter { file ->
                !file.name.contains("moshi-kotlin-codegen")
            }
        }
    }
}

